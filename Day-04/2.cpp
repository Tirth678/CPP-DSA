// find weather the number is odd or even
#include<iostream>
using namespace std;
int main(){
    int num;
    cout<<"enter the number"<<endl;
    cin>>num;
    if(num%2==0){
        cout<<"the number is a even one"<<endl;
    }
    else{
        cout<<"the number is a odd one"<<endl;
    }

    return 0;
}