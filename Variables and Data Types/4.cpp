// Question 4 : Write a program to calculate the area of a circle.
// Input : r (radius)
// Output : (area)
#include<iostream>
using namespace std;
int main(){
    int radius, area;
    cout<<"Enter the radius of the circle"<<endl;
    cin>>radius;
    area = (3.14*radius*radius);
    cout<<"Area of the circle: "<<area<<endl;
    return 0;
}


 